import { useEffect, useState } from 'react';
import { useRouter } from 'next/router';
import { getScanResult } from '../../services/api';
import { ShieldAlert, Crosshair, CheckCircle, Database, AlertCircle, Brain, Layers, Target, Eye } from 'lucide-react';

export default function ResultDetail() {
  const router = useRouter();
  const { id } = router.query;
  
  const [data, setData] = useState(null);
  const [error, setError] = useState('');

  useEffect(() => {
    if (!id) return;
    getScanResult(id)
      .then(setData)
      .catch(err => setError(err.message));
  }, [id]);

  if (error) return <div className="text-red-500 p-8 border border-red-500/30 rounded-lg bg-red-500/10">Error: {error}</div>;
  if (!data) return <div className="text-gray-400 animate-pulse">Decrypting intelligence report...</div>;

  const { scan, detections } = data;
  const isHighRisk = scan.risk_level === 'HIGH';

  // Parse ViT analysis from JSON string
  let vitData = null;
  try {
    vitData = scan.vit_analysis ? JSON.parse(scan.vit_analysis) : null;
  } catch (e) {
    vitData = null;
  }

  const severityColor = (sev) => {
    if (sev === 'HIGH') return 'text-red-500 bg-red-500/10 border-red-500/30';
    if (sev === 'MEDIUM') return 'text-yellow-500 bg-yellow-500/10 border-yellow-500/30';
    return 'text-green-500 bg-green-500/10 border-green-500/30';
  };

  return (
    <div className="space-y-8">
      <div className="flex justify-between items-start">
        <div>
          <h1 className="text-3xl font-bold flex items-center gap-3">
            Intelligence Report 
            <span className="text-gray-500 font-mono text-xl">#INC-{scan.id.toString().padStart(4, '0')}</span>
          </h1>
          <p className="text-gray-400 mt-2">Analysis completed on {new Date(scan.created_at).toLocaleString()}</p>
        </div>
      </div>

      {scan.mismatch_found === 1 && (
        <div className="bg-red-900/20 border border-red-500/50 p-6 rounded-xl flex items-start gap-4 animate-pulse">
            <ShieldAlert size={32} className="text-red-500 shrink-0" />
            <div>
                <h2 className="text-xl font-black text-red-500 uppercase tracking-tighter">Cargo Mismatch Detected</h2>
                <p className="text-gray-300 mt-1">
                    The detected objects in this scan do not align with the declared category: <span className="text-white font-bold">"{scan.declared_cargo}"</span>.
                </p>
            </div>
        </div>
      )}

      <div className="flex flex-col md:flex-row justify-between items-start md:items-end gap-6">
        <div className="flex items-center gap-6">
          <div className={`p-4 rounded-2xl ${scan.risk_level === 'HIGH' ? 'bg-red-600/10 text-red-500 border border-red-600/20' : 'bg-green-600/10 text-green-500 border border-green-600/20'}`}>
            <ShieldAlert size={40} />
          </div>
          <div>
            <div className="text-xs font-bold text-gray-500 uppercase tracking-widest mb-1">Final Risk Assessment</div>
            <div className="text-3xl font-black tracking-tighter flex items-center gap-3">
                {scan.risk_level} 
                <span className="text-gray-600">/</span> 
                <span className="text-blue-500">{scan.risk_score}</span>
            </div>
            <div className="text-[10px] text-gray-500 uppercase tracking-[0.2em] mt-1 font-bold">Intelligence Score Generated by YOLOv8 + ViT</div>
          </div>
        </div>
        
        <div className="flex gap-4">
            <div className="px-5 py-3 bg-gray-800 border border-gray-700 rounded-xl">
                <div className="text-[10px] font-bold text-gray-500 uppercase tracking-widest mb-1">Declared Category</div>
                <div className="text-sm font-bold text-gray-200">{scan.declared_cargo || 'Not Specified'}</div>
            </div>
            <div className="px-5 py-3 bg-gray-800 border border-gray-700 rounded-xl">
                <div className="text-[10px] font-bold text-gray-500 uppercase tracking-widest mb-1">Scan ID</div>
                <div className="text-sm font-bold text-gray-200">#CRG-{scan.id.toString().padStart(4, '0')}</div>
            </div>
        </div>
      </div>

      {scan.risk_explanation && (
        <div className="bg-blue-900/10 border border-blue-500/30 p-4 rounded-lg">
          <h3 className="text-sm font-bold text-blue-400 uppercase tracking-widest mb-1 flex items-center gap-2">
            <AlertCircle size={16} /> AI Intelligence Briefing
          </h3>
          <p className="text-gray-300 italic">" {scan.risk_explanation} "</p>
        </div>
      )}

      {/* ─── DUAL MODEL VIEW: YOLO + ViT ─── */}
      <div className="grid grid-cols-1 lg:grid-cols-2 gap-8">
        {/* YOLO Image Viewer */}
        <div className="bg-gray-800 rounded-xl border border-gray-700 overflow-hidden shadow-xl flex flex-col">
          <div className="p-4 border-b border-gray-700 flex items-center gap-2 bg-gray-900/50">
            <Crosshair size={18} className="text-blue-400" />
            <span className="font-semibold tracking-wide text-sm text-gray-300">YOLOv8 OBJECT DETECTION</span>
          </div>
          <div className="p-6 flex-1 flex items-center justify-center bg-gray-950">
            <img 
              src={`http://localhost:5000/uploads/annotated_${scan.image_url}`} 
              onError={(e) => { e.target.src = `http://localhost:5000/uploads/${scan.image_url}` }}
              alt="YOLO Scan" 
              className="max-w-full rounded border border-gray-800 shadow-2xl" 
            />
          </div>
        </div>

        {/* ViT Attention Heatmap Viewer */}
        <div className="bg-gray-800 rounded-xl border border-purple-500/30 overflow-hidden shadow-xl flex flex-col">
          <div className="p-4 border-b border-purple-500/20 flex items-center gap-2 bg-purple-950/20">
            <Brain size={18} className="text-purple-400" />
            <span className="font-semibold tracking-wide text-sm text-purple-300">VISION TRANSFORMER (ViT-B/16) ATTENTION MAP</span>
          </div>
          <div className="p-6 flex-1 flex items-center justify-center bg-gray-950">
            <img 
              src={`http://localhost:5000/uploads/vit_${scan.image_url}`} 
              onError={(e) => { e.target.src = `http://localhost:5000/uploads/${scan.image_url}` }}
              alt="ViT Attention" 
              className="max-w-full rounded border border-purple-800/50 shadow-2xl" 
            />
          </div>
        </div>
      </div>

      {/* ─── ViT SMUGGLING PATTERN ANALYSIS ─── */}
      {vitData && (
        <div className="bg-gray-800 rounded-xl border border-purple-500/30 shadow-xl overflow-hidden">
          <div className="p-5 border-b border-purple-500/20 bg-purple-950/10 flex items-center justify-between">
            <div className="flex items-center gap-3">
              <Brain size={22} className="text-purple-400" />
              <div>
                <h3 className="font-bold text-purple-300 uppercase tracking-widest text-sm">ViT Smuggling Pattern Analysis</h3>
                <p className="text-[10px] text-gray-500 uppercase tracking-widest mt-0.5">Vision Transformer • Multi-Head Self-Attention • 12 Encoder Layers</p>
              </div>
            </div>
            <div className="flex items-center gap-2 text-right">
              <div>
                <div className="text-[10px] font-bold text-gray-500 uppercase tracking-widest">Smuggle Score</div>
                <div className={`text-3xl font-black tracking-tighter ${vitData.smuggle_score > 50 ? 'text-red-500' : vitData.smuggle_score > 25 ? 'text-yellow-500' : 'text-green-500'}`}>
                  {vitData.smuggle_score}<span className="text-sm text-gray-500">/100</span>
                </div>
              </div>
            </div>
          </div>

          {/* Stats Row */}
          <div className="grid grid-cols-3 border-b border-gray-700/50">
            <div className="p-5 border-r border-gray-700/50 text-center">
              <Target size={20} className="mx-auto text-cyan-400 mb-2" />
              <div className="text-2xl font-black text-white">{vitData.hotspot_count}</div>
              <div className="text-[10px] text-gray-500 uppercase tracking-widest font-bold mt-1">Attention Hotspots</div>
            </div>
            <div className="p-5 border-r border-gray-700/50 text-center">
              <Layers size={20} className="mx-auto text-orange-400 mb-2" />
              <div className="text-2xl font-black text-white">{vitData.attention_variance}</div>
              <div className="text-[10px] text-gray-500 uppercase tracking-widest font-bold mt-1">Attention Variance (σ)</div>
            </div>
            <div className="p-5 text-center">
              <Eye size={20} className="mx-auto text-pink-400 mb-2" />
              <div className="text-2xl font-black text-white">{vitData.concealment_ratio}</div>
              <div className="text-[10px] text-gray-500 uppercase tracking-widest font-bold mt-1">Concealment Ratio</div>
            </div>
          </div>

          {/* Detected Patterns */}
          <div className="p-5">
            <h4 className="text-[10px] font-bold text-gray-500 uppercase tracking-widest mb-4">Detected Smuggling Patterns</h4>
            {vitData.patterns && vitData.patterns.length > 0 ? (
              <div className="space-y-3">
                {vitData.patterns.map((p, i) => (
                  <div key={i} className={`p-4 rounded-lg border ${severityColor(p.severity)} flex items-start gap-3`}>
                    <ShieldAlert size={18} className="shrink-0 mt-0.5" />
                    <div>
                      <div className="font-bold text-sm uppercase tracking-wider">{p.pattern}</div>
                      <div className="text-xs opacity-80 mt-1">{p.detail}</div>
                    </div>
                    <span className="ml-auto text-[10px] font-black uppercase tracking-widest border rounded-full px-3 py-1 shrink-0">{p.severity}</span>
                  </div>
                ))}
              </div>
            ) : (
              <div className="text-center py-8 text-gray-500">
                <CheckCircle size={40} className="mx-auto mb-3 opacity-40" />
                <p className="text-sm">No smuggling patterns detected by Vision Transformer.</p>
              </div>
            )}
          </div>
        </div>
      )}

      {/* ─── OBJECT DETECTIONS TABLE ─── */}
      <div className="bg-gray-800 rounded-xl border border-gray-700 shadow-xl overflow-hidden">
        <div className="p-4 border-b border-gray-700 flex items-center gap-2 bg-gray-900/50">
          <Database size={18} className="text-blue-400" />
          <span className="font-semibold tracking-wide text-sm text-gray-300">IDENTIFIED THREAT SIGNATURES</span>
        </div>
        
        <div className="p-0 overflow-auto max-h-[500px]">
          {detections.length > 0 ? (
            <table className="w-full text-left">
              <thead className="bg-gray-900 sticky top-0 border-b border-gray-700 z-10">
                <tr>
                  <th className="p-4 text-xs font-medium text-gray-400 uppercase tracking-widest">Object</th>
                  <th className="p-4 text-xs font-medium text-gray-400 uppercase tracking-widest">Threat Class</th>
                  <th className="p-4 text-xs font-medium text-gray-400 uppercase tracking-widest text-right">Confidence</th>
                </tr>
              </thead>
              <tbody className="divide-y divide-gray-700/50">
                {detections.map(d => {
                  const cat = d.threat_category || 'UNKNOWN';
                  const catColors = {
                    WEAPON: 'bg-red-500/20 text-red-400 border-red-500/30',
                    FIREARM: 'bg-red-700/20 text-red-300 border-red-700/30',
                    ALCOHOL: 'bg-amber-500/20 text-amber-400 border-amber-500/30',
                    DRUGS_PARAPHERNALIA: 'bg-purple-500/20 text-purple-400 border-purple-500/30',
                    ELECTRONICS: 'bg-blue-500/20 text-blue-400 border-blue-500/30',
                    CONCEALMENT: 'bg-orange-500/20 text-orange-400 border-orange-500/30',
                    HUMAN_INDICATOR: 'bg-red-600/20 text-red-300 border-red-600/30',
                    ANIMAL: 'bg-green-500/20 text-green-400 border-green-500/30',
                    ORGANIC: 'bg-lime-500/20 text-lime-400 border-lime-500/30',
                    VEHICLE: 'bg-cyan-500/20 text-cyan-400 border-cyan-500/30',
                    UNKNOWN: 'bg-gray-500/20 text-gray-400 border-gray-500/30',
                  };
                  return (
                    <tr key={d.id} className="hover:bg-gray-750">
                      <td className="p-4 font-mono font-medium text-gray-200 capitalize">
                        {d.object_name}
                      </td>
                      <td className="p-4">
                        <span className={`text-[10px] font-black uppercase tracking-widest px-3 py-1.5 rounded-full border ${catColors[cat] || catColors.UNKNOWN}`}>
                          {d.threat_label || cat}
                        </span>
                      </td>
                      <td className="p-4 text-right">
                        <div className="flex items-center justify-end gap-3">
                          <div className="w-24 h-1.5 bg-gray-900 rounded-full overflow-hidden">
                            <div 
                              className={`h-full rounded-full ${d.confidence > 0.7 ? 'bg-green-500' : d.confidence > 0.4 ? 'bg-yellow-500' : 'bg-red-500'}`} 
                              style={{ width: `${d.confidence * 100}%` }}
                            />
                          </div>
                          <span className="text-xs font-mono w-8 text-gray-400">
                            {(d.confidence * 100).toFixed(0)}%
                          </span>
                        </div>
                      </td>
                    </tr>
                  );
                })}
              </tbody>
            </table>
          ) : (
            <div className="p-12 text-center text-gray-500 flex flex-col items-center">
              <CheckCircle size={48} className="text-gray-700 mb-4 opacity-50" />
              <p>No objects detected above threshold.</p>
            </div>
          )}
        </div>
      </div>
    </div>
  );
}

